t = int(input())
# n = 1 -> 1
# n = 2 -> 2
# n = 3 -> 4
# - 1+1+1,1+2,2+1,3
# n = 4 -> 7
# 1 + 3 -> n = 3 인 경우랑 같음
# 2 + 2 -> n = 2 인 경우랑 같음
# 3 + 1 -> n = 1 인 경우랑 같음
# 즉 d[4] = d[3] + d[2] + d[1]
for _ in range(t):
    n = int(input())

    d = [0] * (11)

    d[1] = 1
    d[2] = 2
    d[3] = 4

    for i in range(4,n+1):
        d[i] = d[i-1] + d[i-2] + d[i-3]

    print(d[n])
